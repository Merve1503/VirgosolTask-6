public class Task17Person {

    /*
    17.Bir sınıf oluşturun ve bu sınıfta private bir değişken tanımlayın.
    Bu değişkeni getter ve setter metodları ile güncelleyin ve ekrana yazdırın.
     */

    // Private bir değişken tanımlayın
    private String name;

    // Getter metodu tanımlayın
    public String getName() {
        return name;
    }

    // Setter metodu tanımlayın
    public void setName(String name) {
        this.name = name;
    }
}
